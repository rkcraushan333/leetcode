class Solution{
    public:
    int setSetBit(int x, int y, int l, int r){
        // code here
        int t = r-l+1;
        l--;
        y >>= l;
        while((t--)&&y)
        {
            if(y&1)
            {
                x |= (1<<l);
            }
            l++;
            y >>= 1;
        }
        return x;
    }
};

